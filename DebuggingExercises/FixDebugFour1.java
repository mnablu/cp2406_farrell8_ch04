// This program assigns values to two variables
// and performs mathematical operations with them
public class FixDebugFour1
{
   public static void main(String args[])
   {
      int x = 5;
      int y = 8;
      int z = 19;

      System.out.println("adding x and y is " + (x + y));
      System.out.println("subtracting x and y is " + (z - y));
      System.out.println("dividing x and y is " + (z / x));
      System.out.println("multiplying x and y is " + (x * z));
   }
}